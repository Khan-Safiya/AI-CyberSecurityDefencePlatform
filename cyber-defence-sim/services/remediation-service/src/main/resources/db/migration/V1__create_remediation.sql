CREATE SCHEMA IF NOT EXISTS remediation;

CREATE TABLE remediation.remediation_actions (
    id UUID PRIMARY KEY,
    simulation_id UUID NOT NULL,
    round_id UUID,
    vulnerability_id UUID NOT NULL,
    detection_id UUID,
    agent_id UUID NOT NULL,
    target_id UUID NOT NULL,
    remediation_type VARCHAR(60) NOT NULL,
    patch_summary VARCHAR(4000) NOT NULL,
    status VARCHAR(30) NOT NULL,
    outcome_summary VARCHAR(2000),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL,
    approved_at TIMESTAMP WITH TIME ZONE,
    applied_at TIMESTAMP WITH TIME ZONE,
    verified_at TIMESTAMP WITH TIME ZONE,
    rolled_back_at TIMESTAMP WITH TIME ZONE,
    CONSTRAINT remediation_type_check CHECK (remediation_type IN (
        'AUTH_REQUIRED', 'OBJECT_AUTHORIZATION', 'RATE_LIMIT', 'DISABLE_DEBUG_ENDPOINT',
        'INPUT_VALIDATION', 'UPDATE_DEPENDENCY_METADATA'
    )),
    CONSTRAINT remediation_status_check CHECK (status IN (
        'PROPOSED', 'APPROVED', 'APPLIED', 'VERIFIED', 'FAILED', 'ROLLED_BACK'
    ))
);

CREATE INDEX remediation_simulation_created_idx
    ON remediation.remediation_actions (simulation_id, created_at, id);
CREATE INDEX remediation_vulnerability_idx
    ON remediation.remediation_actions (vulnerability_id);
CREATE INDEX remediation_detection_idx
    ON remediation.remediation_actions (detection_id);

